function sortearCarta() {
    var numeroCartaMaquina = parseInt(Math.random() * 29)    
    cartaMaquina = cartas[numeroCartaMaquina]


    var numeroCartaJogador = parseInt(Math.random() * 29)  
    while(numeroCartaMaquina == numeroCartaJogador){
        numeroCartaJogador = parseInt(Math.random() * 29)
    } 

    cartaJogador = cartas[numeroCartaJogador]
    console.log(cartaJogador)

    document.getElementById('btnSortear').disabled = true
    document.getElementById('btnJogar').disabled = false

    exibirCartaJogador()
}


function obtemAtributoSelecionado(){
 var radioAtributos = document.getElementsByName('atributo')

for (var i = 0; i < radioAtributos.length; i++){
if (radioAtributos[i].checked == true){
    return radioAtributos[i].value
  }
 }
}


function jogar(){
    var atributoSelcionado = obtemAtributoSelecionado()
    var res = document.getElementById('resultado')
    var valorCartaJogador = cartaJogador.atributos[atributoSelcionado]
    var valorCartaMaquina = cartaMaquina.atributos[atributoSelcionado]

    if (valorCartaJogador > valorCartaMaquina){
        htmlResultados = "<p class='resultado-final'>Você Venceu!</p>"
       
    } else if (valorCartaJogador < valorCartaMaquina){
        htmlResultados = "<p class='resultado-final'>Você Perdeu!</p>"
     
    }else{
        htmlResultados = "<p class='resultado-final'>Empatou!</p>"
      
    }

    res.innerHTML = htmlResultados

    document.getElementById('btnJogar').disabled = true

    exibirCartaMaquina()
}

function exibirCartaJogador(){
   var divCartaJogador = document.getElementById('carta-jogador')
   divCartaJogador.style.backgroundImage = `url(${cartaJogador.imagem})`

   var moldura ='<img src="https://www.alura.com.br/assets/img/imersoes/dev-2021/card-super-trunfo-transparent.png" style=" width: inherit; height: inherit; position: absolute;">';

   var tagHTML = "<div id='opcoes' class='carta-status'>"

   var opcoesTexto = ''
 for(var atributo in cartaJogador.atributos){
     opcoesTexto += "<input type='radio' name='atributo'value='" +atributo + "'>" + atributo + " " + cartaJogador.atributos[atributo] + "<br>";
  }
  var nome = `<p class="carta-subtitle">${cartaJogador.nome}</p>`

  divCartaJogador.innerHTML = moldura + nome + tagHTML + opcoesTexto + "</div>";

}

